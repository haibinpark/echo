    /**
     * 压缩bitmap(从本地图片)
     *
     * @param imagePath 本地图片路径
     * @param scale     压缩比率 可以为空,那么是默认是200,越大压缩比越小
     * @return 一个缩放好的bitmap
     */
    public static Bitmap fileZoomBitmap(String imagePath, Float scale) {
        if (!new File(imagePath).exists()) {
            return null;
        }

        if (scale == null) {
            scale = 200f;
        }

        BitmapFactory.Options options = new BitmapFactory.Options();
        options.inJustDecodeBounds = true;
        Bitmap bm = decodeFile(imagePath, options);
        options.inJustDecodeBounds = false;
        int be = (int) (options.outHeight / scale);
        if (be <= 1) {
            be = 1;
        }
        options.inSampleSize = be;// be=2.表示压缩为原来的1/2,以此类推
        bm = decodeFile(imagePath, options);
        return bm;
    }

    /**
     * 压缩bitmap(从res图片)
     *
     * @param res
     * @param drawable
     * @param scale
     * @return
     */
    public static Bitmap resZoomBitmap(Resources res, int drawable, Float scale) {
        BitmapFactory.Options options = new BitmapFactory.Options();
        options.inJustDecodeBounds = true;
        Bitmap bm = BitmapFactory.decodeResource(res, drawable, options);
        options.inJustDecodeBounds = false;
        //这里可以得到bm的狂傲尺寸信息而不需要把bm加载到内存中
        int be = (int) (options.outHeight / scale);
        if (be <= 1) {
            be = 1;
        }
        options.inSampleSize = be;// be=2.表示压缩为原来的1/2,以此类推
        bm = BitmapFactory.decodeResource(res, drawable, options);
        return bm;
    }

    /**
     * 压缩文件
     *
     * @param f
     * @param mBitmap
     */
    public static File bitmapZoomFile(File f, Bitmap mBitmap) {
        try {
            f.createNewFile();
            FileOutputStream fOut = new FileOutputStream(f);
            //bitmap和流压缩到文件中,100为不压缩,50即压缩50%
            mBitmap.compress(Bitmap.CompressFormat.PNG, 100, fOut);
            fOut.flush();
            fOut.close();
        } catch (IOException e) {
            e.printStackTrace();
        }
        return f;
    }

    /**
     * 旋转Bitmap(照相用)
     *
     * @param b
     * @param rotateDegree
     * @return
     */
    public static Bitmap getRotateBitmap(Bitmap b, float rotateDegree) {
        Matrix matrix = new Matrix();
        matrix.postRotate(rotateDegree);
        Bitmap rotaBitmap = Bitmap.createBitmap(b, 0, 0, b.getWidth(), b.getHeight(), matrix, false);
        return rotaBitmap;
    }

    /**
     * 删除文件夹下所有文件
     *
     * @param path
     * @return
     */
    public static boolean delAllFile(String path) {
        // 删除指定文件夹下所有文件
        // param path 文件夹完整绝对路径
        boolean flag = false;
        File file = new File(path);
        if (!file.exists()) {
            return flag;
        }
        if (!file.isDirectory()) {
            return flag;
        }
        String[] tempList = file.list();
        File temp = null;
        for (int i = 0; i < tempList.length; i++) {
            if (path.endsWith(File.separator)) {
                temp = new File(path + tempList[i]);
            } else {
                temp = new File(path + File.separator + tempList[i]);
            }
            if (temp.isFile()) {
                temp.delete();
            }
            if (temp.isDirectory()) {
                delAllFile(path + "/" + tempList[i]);// 先删除文件夹里面的文件
                delFolder(path + "/" + tempList[i]);// 再删除空文件夹
                flag = true;
            }
        }
        return flag;
    }


    /**
     * 删除指定文件夹
     *
     * @param folderPath
     */
    public static void delFolder(String folderPath) {
        // 删除文件夹
        // param folderPath 文件夹完整绝对路径
        try {
            delAllFile(folderPath); // 删除完里面所有内容
            String filePath = folderPath;
            filePath = filePath.toString();
            File myFilePath = new File(filePath);
            myFilePath.delete(); // 删除空文件夹
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

    /**
     * 删除指定文件
     *
     * @param file
     */
    public static void deleteFile(File file) {
        if (file.exists()) { // 判断文件是否存在
            if (file.isFile()) { // 判断是否是文件
                file.delete(); // delete()方法 你应该知道 是删除的意思;
            } else if (file.isDirectory()) { // 否则如果它是一个目录
                File files[] = file.listFiles(); // 声明目录下所有的文件 files[];
                for (int i = 0; i < files.length; i++) { // 遍历目录下所有的文件
                    deleteFile(files[i]); // 把每个文件 用这个方法进行迭代
                }
            }
            file.delete();
        } else {
            LogUtil.i("文件不存在");
        }
    }

    /**
     * 对扫脸获取的图片字节数组第一次压缩,防止OOM和卡顿
     *
     * @param imgByte
     * @return
     */
    public static Bitmap byteToBitmap(byte[] imgByte) {

        BitmapFactory.Options options = new BitmapFactory.Options();
        options.inJustDecodeBounds = true;
        BitmapFactory.decodeByteArray(imgByte, 0, imgByte.length, options);
        //根据参数128*128的值改变inSampleSize的大小,参数越大inSampleSize相对越大
        options.inSampleSize = MImageUtil.computeSampleSize(options, -1, 500 * 500);
        options.inJustDecodeBounds = false;
        Bitmap bitmap = BitmapFactory.decodeByteArray(imgByte, 0, imgByte.length, options);
        LogUtil.i("缩放比例:" + options.inSampleSize);

        return bitmap;
    }

    /**
     * 根据安卓源码得来的获取最佳inSampleSize缩小比例的方法
     *
     * @param options
     * @param minSideLength
     * @param maxNumOfPixels
     * @return
     */
    public static int computeSampleSize(BitmapFactory.Options options, int minSideLength, int maxNumOfPixels) {
        int initialSize = computeInitialSampleSize(options, minSideLength, maxNumOfPixels);
        int roundedSize;
        if (initialSize <= 8) {
            roundedSize = 1;
            while (roundedSize < initialSize) {
                roundedSize <<= 1;
            }
        } else {
            roundedSize = (initialSize + 7) / 8 * 8;
        }
        return roundedSize;
    }

    private static int computeInitialSampleSize(BitmapFactory.Options options, int minSideLength, int maxNumOfPixels) {
        double w = options.outWidth;
        double h = options.outHeight;
        int lowerBound = (maxNumOfPixels == -1) ? 1 : (int) Math.ceil(Math.sqrt(w * h / maxNumOfPixels));
        int upperBound = (minSideLength == -1) ? 128 : (int) Math.min(Math.floor(w / minSideLength), Math.floor(h / minSideLength));
        if (upperBound < lowerBound) {
            // return the larger one when there is no overlapping zone.
            return lowerBound;
        }
        if ((maxNumOfPixels == -1) && (minSideLength == -1)) {
            return 1;
        } else if (minSideLength == -1) {
            return lowerBound;
        } else {
            return upperBound;
        }
    }

